fun main () {
//soma
    var soma = 10
    soma += 10

    //subtração
    var sub = 10
    sub -= 10

    //divisão
    var di = 10
    di /= 10

    //multiplicação
    var multi = 10
    multi *= 10

    //resto da divisão

    var res = 10
    res %= 10

    //incremento

    var incre = 10
    incre++

    //decremento
    var decre = 10
    decre--

    println("$soma $sub $di $multi $res $incre $decre")



}